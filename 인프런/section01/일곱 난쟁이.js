/*
    일곱 난쟁이

    왕비를 피해 일곱 난쟁이들과 함께 평화롭게 생활하고 있던 백설공주에게 위기가 찾아왔습니다.
    일과를 마치고 돌아온 난쟁이가 일곱 명이 아닌 아홉 명이었던 것입니다.
    아홉 명의 난쟁이는 모두 자신이 "백설 공주와 일곱 난쟁이"의 주인공이라고 주장했습니다.
    뛰어난 수학적 직관력을 가지고 있던 백설공주는, 다행스럽게도 일곱 난쟁이의 키의 합이 100
    이 됨을 기억해 냈습니다.
    아홉 난쟁이의 키가 주어졌을 때, 백설공주를 도와 일곱 난쟁이를 찾는 프로그램을 작성하세요.

    단, 키는 100을 넘지 않는 모두 다른 자연수이며, 가능한 정답이 여러 가지인 경우에는 아무
    거나 출력합니다.


             input                                   output
    ---------------------------------------------------------------------
    arr
    ---------------------------------------------------------------------
    [22, 7, 21, 19, 10, 15, 25, 8, 13]   [ 22, 7, 10, 15, 25, 8, 13 ]
*/

function solution(arr) {
  let answer = arr
  let sum = answer.reduce((a, b) => a + b, 0)
  let flag = 0
  for (let i = 0; i < 8; i++) {
    for (let j = i + 1; j < 9; j++) {
      if (sum - (answer[i] + answer[j]) == 100) {
        answer.splice(j, 1)
        answer.splice(i, 1)
        flag = 1
        break
      }
    }
    if (flag == 1) break
  }
  return answer
}

solution([22, 7, 21, 19, 10, 15, 25, 8, 13])
